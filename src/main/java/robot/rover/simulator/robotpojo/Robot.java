package robot.rover.simulator.robotpojo;

public class Robot {
	
	private int xUnit,yUnit;	
	private String direction;
	
	
	public String getDirection() {
		return direction;
	}
	public void setDirection(String direction) {
		this.direction = direction;
	}
	public int getyUnit() {
		return yUnit;
	}
	public void setyUnit(int yUnit) {
		this.yUnit = yUnit;
	}
	public int getxUnit() {
		return xUnit;
	}
	public void setxUnit(int xUnit) {
		this.xUnit = xUnit;
	}
	
}
